    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        if (txtName.getText().equals("") || txtAge.getText().equals("") || txtEmployee.getText().equals("") || txtPhone.getText().equals("")||txtEmail.getText().equals("")||txtGender.getText().equals("")||txtLevel.getText().equals("")||txtPosition.getText().equals("")||txtTeam.getText().equals("")||txtDate.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Please Enter All Data");
        } else {
            String data[] = {txtName.getText(), txtAge.getText(), txtEmployee.getText(), txtPhone.getText(), txtEmail.getText(), txtGender.getText(), txtLevel.getText(), txtPosition.getText(), txtTeam.getText(), txtDate.getText()};

            DefaultTableModel tblModel = (DefaultTableModel) jTable2.getModel();

            tblModel.addRow(data);

            JOptionPane.showMessageDialog(this, "Add Data Successfully");
            txtName.setText("");
            txtAge.setText("");
            txtEmployee.setText("");
            txtPhone.setText("");
            txtEmail.setText("");
            txtGender.setText("");
            txtLevel.setText("");
            txtPosition.setText("");
            txtTeam.setText("");
            txtDate.setText("");

        }
    }//GEN-LAST:event_btnSubmitActionPerformed

