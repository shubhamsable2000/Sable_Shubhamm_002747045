    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tblModel = (DefaultTableModel) jTable2.getModel();
        if (jTable2.getSelectedRowCount() == 1) {

            String ename = txtName.getText();
            String eAge = txtAge.getText();
            String EmployeeID = txtEmployee.getText();
            String EPhoneNo = txtPhone.getText();
            String Email = txtEmail.getText();
            String Gender = txtGender.getText();
            String Level = txtLevel.getText();
            String Position= txtPosition.getText();
            String Team = txtTeam.getText();
            String Date = txtDate.getText();

            tblModel.setValueAt(ename, jTable2.getSelectedRow(),0);
            tblModel.setValueAt(eAge, jTable2.getSelectedRow(), 1);
            tblModel.setValueAt(EmployeeID, jTable2.getSelectedRow(), 2);
            tblModel.setValueAt(EPhoneNo, jTable2.getSelectedRow(), 3);
            tblModel.setValueAt(Email, jTable2.getSelectedRow(), 4);
            tblModel.setValueAt(Gender, jTable2.getSelectedRow(), 5);
            tblModel.setValueAt(Level, jTable2.getSelectedRow(), 6);
            tblModel.setValueAt(Position, jTable2.getSelectedRow(), 7);
            tblModel.setValueAt(Team, jTable2.getSelectedRow(), 8);
            tblModel.setValueAt(Date, jTable2.getSelectedRow(), 9);

        } else {
            if (jTable2.getRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "Table is Empty");
            } else {
                JOptionPane.showMessageDialog(this, "Please select single row");
            }

        }

    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tblModel = (DefaultTableModel) jTable2.getModel();

        if (jTable2.getSelectedRowCount() == 1) {
            tblModel.removeRow(jTable2.getSelectedRow());
        } else {
            if (jTable2.getRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "Table Is Empty");
            } else {
                JOptionPane.showMessageDialog(this, "Please select row for delete");

            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

