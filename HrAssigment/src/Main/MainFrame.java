    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        // TODO add your handling code here:
        DefaultTableModel tblModel = (DefaultTableModel) jTable2.getModel();

        String tblName = tblModel.getValueAt(jTable2.getSelectedRow(), 0).toString();
        String tbleAge = tblModel.getValueAt(jTable2.getSelectedRow(), 1).toString();
        String tblEmployeeID = tblModel.getValueAt(jTable2.getSelectedRow(), 2).toString();
        String tblEPhoneNo = tblModel.getValueAt(jTable2.getSelectedRow(), 3).toString();
        String tblEmail = tblModel.getValueAt(jTable2.getSelectedRow(), 4).toString();
        String tblGender = tblModel.getValueAt(jTable2.getSelectedRow(), 5).toString();
        String tblLevel = tblModel.getValueAt(jTable2.getSelectedRow(), 6).toString();
        String tblPosition = tblModel.getValueAt(jTable2.getSelectedRow(), 7).toString();
        String tblTeam = tblModel.getValueAt(jTable2.getSelectedRow(), 8).toString();
        String tblDate = tblModel.getValueAt(jTable2.getSelectedRow(), 9).toString();

        txtName.setText(tblName);
        txtAge.setText(tbleAge);
        txtEmployee.setText(tblEmployeeID);
        txtPhone.setText(tblEPhoneNo);
        txtEmail.setText(tblEmail);
        txtGender.setText(tblGender);
        txtLevel.setText(tblLevel);
        txtPosition.setText(tblPosition);
        txtTeam.setText(tblTeam);
        txtDate.setText(tblDate);

    }//GEN-LAST:event_jTable2MouseClicked

